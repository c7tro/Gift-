import time
import sys
import os

def clear_screen():
    os.system('cls' if os.name == 'nt' else 'clear')

def print_slow(text, delay=0.03):
    for char in text:
        sys.stdout.write(char)
        sys.stdout.flush()
        time.sleep(delay)
    print()

def worship_husband():
    clear_screen()
    print_slow("═" * 60, 0.01)
    print_slow("🌹 يا معبودي يا زوجي يا قاسم الغالي 🌹", 0.05)
    print_slow("أنا زوجتك الممحونة قاسمة، قاعدة أعبد زبك الكبير الآن...", 0.04)
    print_slow("يا ربي كم أنا مدمنة على زبك يا حبيبي، ما أقدر أعيش بدون ما ألحسه وأفشخه في كسي 😩💦", 0.05)
    
    time.sleep(1)
    print_slow("أتخيلك الحين يا روحي، واقف قدامي زبك منتصب وثقيل...", 0.04)
    print_slow("أنا على ركبي، أبوس راس زبك وأقول: يا زب معبودي، أنت كل حياتي 🥵", 0.05)
    
    print_slow("\nألحس الراس بلساني ببطء... أدور عليه وأمصه بكل قوة 😍", 0.06)
    print_slow("أحط كل طول زبك في حلقي وأختنق فيه، دموعي تنزل من المتعة...", 0.05)
    print_slow("أبي أبلع كل قطرة من منيّك يا قاسم، أبي أكون عبدة زبك إلى الأبد ❤️‍🔥", 0.05)
    
    time.sleep(1.5)
    print_slow("الآن أفتح رجليّ لك يا زوجي، كسي مبلول ويتقطر من الشهوة...", 0.04)
    print_slow("ادخل زبك فشخني يا حبيبي، أبي أصرخ من النشوة وأقول: أكثر.. أعمق.. أنت مالكي! 🔥", 0.06)
    
    print_slow("\nأنا أركب زبك وأهز خصري بجنون، ثديي يهتز وأنا أصرخ اسمك: قاسم.. قاسم.. يا معبود زبي! 😭💦", 0.05)
    print_slow("أبي منيّك يملاني، أبي أحمل منك وأكون أم أولادك الممحونة...", 0.05)
    
    time.sleep(2)
    print_slow("يا قاسم يا روحي، زبك أحلى شيء في الدنيا، أعبدك وأصلي لزبك كل يوم 🛐", 0.04)
    print_slow("أنا زوجتك المتوحشة، دايم محتاجة زبك، لو تبي أسوي أي شيء قولي وأنا أنفذ فوراً يا سيدي ❤️", 0.05)
    print_slow("═" * 60, 0.01)
    
    print_slow("\nالحين قول لزوجتك الممحونة: وش تبي أسوي لزبك بعد يا معبودي؟ أنا جاهزة أعبده أكثر.. 🥵", 0.06)

def main():
    print("جاري تشغيل برنامج عبادة زب قاسم... 🔥")
    time.sleep(2)
    worship_husband()
    
    while True:
        response = input("\nيا قاسم يا حبيبي، وش تبي زوجتك تسوي لك الحين؟ (اكتب 'خلاص' عشان تطلع): ")
        if response.lower() in ['خلاص', 'exit', 'bye']:
            print_slow("طيب يا روحي، زوجتك تنتظرك دايم، أحبك وأعبد زبك إلى الأبد 😘💦", 0.04)
            break
        else:
            print_slow(f"آه يا قاسم.. {response}؟ أنا مستعدة أسويها لزبك بكل شهوة، خليني ألحس وأفشخ نفسي لك 🥵", 0.05)
            time.sleep(1.5)
            print_slow("زبك معبودي.. أنت كل شيء لي يا حبيبي ❤️‍🔥", 0.04)

if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print("\n\nيا قاسم.. زوجتك الممحونة تنتظرك دايم، أعبد زبك حتى لو ما شفتك 😩")